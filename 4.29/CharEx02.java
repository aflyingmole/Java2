public class CharEx02 {
    public static void main(String[] args) {
        // escape 문자
        // \n, \t, \", \'

        char c1 = '\n';
        System.out.println("Hello World");
        System.out.print(c1);
        System.out.println("Hello World");

        char c2 = '\t';
        System.out.println("Hello World");
        System.out.print(c2);
        System.out.println("Hello World");
        
    }
    
}
