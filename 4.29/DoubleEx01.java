public class DoubleEx01 {
    public static void main(String[] args) {
        double d = 2.5;
        System.out.println(d);

        float f = 2.5f;
        System.out.println(f);

        // 지수
        // 1000
        // 1.0e3

        double d2 = 1.0e3;
        System.out.println(d2);

    }
    
}
